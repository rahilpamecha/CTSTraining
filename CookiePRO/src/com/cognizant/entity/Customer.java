package com.cognizant.entity;

public class Customer {
	private int custId;
	private int pin;
	
	public Customer(){}

	public int getCustId() {
		return custId;
	}

	public void setCustId(int custId) {
		this.custId = custId;
	}

	public int getPin() {
		return pin;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}
	
	

}
